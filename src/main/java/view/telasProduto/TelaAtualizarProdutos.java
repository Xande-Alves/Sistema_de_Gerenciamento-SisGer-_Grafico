    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        int idProduto = (Integer) jSpinnerIdProduto.getValue();

        if (idProduto<=0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, digite o ID do produto para buscar.", "Campo Vazio", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Faz a chamada ao Controlador que configuramos anteriormente
        produtoAtual = ControladorProduto.getInstanciaControladorProduto().buscarProdutos(idProduto);

        if (produtoAtual == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Produto não encontrado no sistema.", "Não Localizado", javax.swing.JOptionPane.ERROR_MESSAGE);
            jButtonAtualizar.setEnabled(false);
            return;
        }

        // Bloqueia o escutador para não ativar o botão de "Atualizar" enquanto os dados entram nos campos
        escutadorAtivo = false;

        jTextFieldNome.setText(produtoAtual.getNome());
        jSpinnerIdFornecedor.setValue(produtoAtual.getIdFornecedor());
        jTextAreaDescricao.setText(produtoAtual.getDescricao());
        jFormattedTextFieldPrecoCompra.setValue(produtoAtual.getPrecoCompra());
        jFormattedTextFieldQuantidadeEstoque.setValue(produtoAtual.getQuantidadeEstoque());

        // Libera o escutador. Qualquer modificação que o usuário fizer a partir de agora ativará o botão "Atualizar"
        escutadorAtivo = true;
        jButtonAtualizar.setEnabled(false);
    }//GEN-LAST:event_jButtonBuscarActionPerformed


