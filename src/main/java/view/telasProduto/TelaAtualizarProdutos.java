    private void configurarMascaras() {
        // =================================================================
        // FORMATADOR MONETÁRIO E DE QUANTIDADE
        // =================================================================
        java.text.NumberFormat formatoMoeda = java.text.NumberFormat.getCurrencyInstance();
        formatoMoeda.setMinimumFractionDigits(2);
        formatoMoeda.setMaximumFractionDigits(2);

        javax.swing.text.NumberFormatter formatterSalario = new javax.swing.text.NumberFormatter(formatoMoeda);
        formatterSalario.setAllowsInvalid(false); // Impede digitação de letras
        formatterSalario.setOverwriteMode(true);

        jFormattedTextFieldPrecoCompra.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(formatterSalario));

        DecimalFormat formatoNumero = new DecimalFormat("0.00");

        NumberFormatter formatter = new NumberFormatter(formatoNumero);
        formatter.setValueClass(Double.class);
        formatter.setAllowsInvalid(false);
        formatter.setMinimum(0.0);
        formatter.setOverwriteMode(true);
        jFormattedTextFieldQuantidadeEstoque.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(formatter));

        // Define um valor inicial numérico para evitar erros de inicialização vazia
        jFormattedTextFieldPrecoCompra.setValue(0.00);
        jFormattedTextFieldQuantidadeEstoque.setValue(0.00);
    }

    // Monitora se o usuário alterar qualquer dado após a busca
    private void configurarEscutadoresDeModificacao() {
        DocumentListener dl = new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                verificarAlteracao();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                verificarAlteracao();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                verificarAlteracao();
            }
        };

        jTextFieldNome.getDocument().addDocumentListener(dl);
        jTextAreaDescricao.getDocument().addDocumentListener(dl);
        jFormattedTextFieldPrecoCompra.getDocument().addDocumentListener(dl);
        jFormattedTextFieldQuantidadeEstoque.getDocument().addDocumentListener(dl);
        
        // Listener específico do spinner
        jSpinnerIdFornecedor.addChangeListener(e -> verificarAlteracao());
    }

    private void verificarAlteracao() {
        if (escutadorAtivo && produtoAtual != null) {
            jButtonAtualizar.setEnabled(true);
        }
    }

