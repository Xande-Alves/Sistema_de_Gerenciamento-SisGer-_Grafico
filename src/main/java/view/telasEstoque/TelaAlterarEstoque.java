    private void jButtonAlterarEstoqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarEstoqueActionPerformed
        // RECUPERA QUANTIDADE ESTOQUE
        double quantidadeAteraEstoque = 0.0;
        if (jFormattedTextFieldQuantidadeAlterada.getValue() != null) {
            quantidadeAteraEstoque = ((Number) jFormattedTextFieldQuantidadeAlterada.getValue()).doubleValue();
        }

        // =================================================================
        // VALIDAÇÃO: VERIFICAÇÃO DE CAMPOS VAZIOS
        // =================================================================
        if (quantidadeAteraEstoque == 0) {

            javax.swing.JOptionPane.showMessageDialog(this,
                "Por favor, o valor deve ser diferente de 0!",
                "Valor nulo",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return; // Interrompe a execução aqui se faltar algo
        }

        // 1. Envia os dados para salvar
        controladores.ControladorEstoque.getInstanciaControladorEstoque().alteraEstoqueProduto(produtoAtual, quantidadeAteraEstoque);

        // =================================================================
        // NOVOS COMPORTAMENTOS: AVISO
        // =================================================================
        // 2. Mostra a caixinha de aviso de Sucesso
        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Estoque atualizado com sucesso!",
            "Sucesso",
            javax.swing.JOptionPane.INFORMATION_MESSAGE
        );

        jSpinnerIdProduto.requestFocus();

        // Desabilita de novo até a próxima alteração
        jButtonAlterarEstoque.setEnabled(false);
    }//GEN-LAST:event_jButtonAlterarEstoqueActionPerformed

    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        int idProduto = (Integer) jSpinnerIdProduto.getValue();

        if (idProduto<=0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, digite o ID do produto para buscar.", "Campo Vazio", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Faz a chamada ao Controlador que configuramos anteriormente
        produtoAtual = ControladorProduto.getInstanciaControladorProduto().buscarProdutos(idProduto);

        if (produtoAtual == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Produto não encontrado no sistema.", "Não Localizado", javax.swing.JOptionPane.ERROR_MESSAGE);
            jButtonAlterarEstoque.setEnabled(false);
            return;
        }

        jLabelValorNomeProduto.setText(produtoAtual.getNome());
        jLabelValorIdFornecedor.setText(String.valueOf(produtoAtual.getIdFornecedor()));
        jLabelValorDescricao.setText(produtoAtual.getDescricao());
        jLabelValorPrecoCompra.setText("R$ " + String.valueOf(produtoAtual.getPrecoCompra()));
        jLabelValorQuantidadeEstoque.setText(String.valueOf(produtoAtual.getQuantidadeEstoque()));

        // LibejLabelValorIdFornecedorcutador. Qualquer modificação que o usuário fizer a partir de agora ativará o botão "Atualizar"
        escutadorAtivo = true;
        jButtonAlterarEstoque.setEnabled(false);
    }//GEN-LAST:event_jButtonBuscarActionPerformed


