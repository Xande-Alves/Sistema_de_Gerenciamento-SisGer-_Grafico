    // Monitora se o usuário alterar qualquer dado após a busca
    private void configurarEscutadoresDeModificacao() {
        DocumentListener dl = new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                verificarAlteracao();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                verificarAlteracao();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                verificarAlteracao();
            }
        };

        jFormattedTextFieldQuantidadeAlterada.getDocument().addDocumentListener(dl);
        
    }

    private void verificarAlteracao() {
        if (escutadorAtivo && produtoAtual != null) {
            jButtonAlterarEstoque.setEnabled(true);
        }
    }

