    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        int idVenda = (Integer) jSpinnerIdVenda.getValue();

        if (idVenda <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, digite o ID da venda para buscar.", "Campo Vazio", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Faz a chamada ao Controlador que configuramos anteriormente
        vendaAtual = ControladorVenda.getInstanciaControladorVenda().buscarVendaRepositorio(idVenda);

        if (vendaAtual == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Venda não encontrada no sistema.", "Não Localizada", javax.swing.JOptionPane.ERROR_MESSAGE);
            jButtonDesativar.setEnabled(false);
            return;
        }

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");

        jLabelValorData.setText(vendaAtual.getDataVenda().format(formatter));
        jLabelValorIdVenda.setText(String.valueOf(vendaAtual.getIdVenda()));
        jLabelValorIdCliente.setText(String.valueOf(vendaAtual.getIdClienteVenda()));
        jLabelValorIdVendedor.setText(String.valueOf(vendaAtual.getIdVendedorVenda()));
        jLabelValorSituacao.setText(vendaAtual.getVendaAtiva() ? "Ativa" : "Cancelada");
        jLabelValorValorTotal.setText("R$" + vendaAtual.getValorTotalVenda());

        jButtonDesativar.setEnabled(true);
    }//GEN-LAST:event_jButtonBuscarActionPerformed

