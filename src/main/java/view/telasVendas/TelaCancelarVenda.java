    private void jButtonDesativarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDesativarActionPerformed
        int resposta = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Deseja desativar a venda?",
                "Confirmar cancelamento",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (resposta == javax.swing.JOptionPane.YES_OPTION) {
            ControladorVenda.getInstanciaControladorVenda().cancelarVenda(vendaAtual.getIdVenda());
            
            jLabelValorSituacao.setText(vendaAtual.getVendaAtiva() ? "Ativa" : "Cancelada");
            
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Venda desativada com sucesso!",
                "Sucesso",
                javax.swing.JOptionPane.INFORMATION_MESSAGE
        );
        }
    }//GEN-LAST:event_jButtonDesativarActionPerformed


